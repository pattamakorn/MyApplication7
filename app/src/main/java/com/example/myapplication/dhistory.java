package com.example.myapplication;

public class dhistory {

    String hidhis,hnametree,hweight,hdatet,hstatus,himg;

    public dhistory() {
    }

    public dhistory(String hidhis, String hnametree, String hweight, String hdatet, String hstatus,String himg) {
        this.hidhis = hidhis;
        this.hnametree = hnametree;
        this.hweight = hweight;
        this.hdatet = hdatet;
        this.hstatus = hstatus;
        this.himg = himg;
    }

    public String getHidhis() {
        return hidhis;
    }

    public void setHidhis(String hidhis) {
        this.hidhis = hidhis;
    }

    public String getHnametree() {
        return hnametree;
    }

    public void setHnametree(String hnametree) {
        this.hnametree = hnametree;
    }

    public String getHweight() {
        return hweight;
    }

    public void setHweight(String hweight) {
        this.hweight = hweight;
    }

    public String getHdatet() {
        return hdatet;
    }

    public void setHdatet(String hdatet) {
        this.hdatet = hdatet;
    }

    public String getHstatus() {
        return hstatus;
    }

    public void setHstatus(String hstatus) {
        this.hstatus = hstatus;
    }

    public String getHimg() {
        return himg;
    }

    public void setHimg(String himg) {
        this.himg = himg;
    }
}
